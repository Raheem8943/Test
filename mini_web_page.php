<p><font size="4"><b>Wildlife</b> refers to all undomesticated animals and plants found in natural environments. This includes mammals, birds, reptiles, amphibians, insects, and marine life, as well as wild plants and fungi. Wildlife plays a crucial role in maintaining ecological balance by supporting food chains, pollination, seed dispersal, and nutrient cycling.

Wildlife habitats range from forests, deserts, and grasslands to oceans and freshwater ecosystems. Human activities such as deforestation, pollution, poaching, climate change, and urbanization threaten wildlife around the world.

Efforts to conserve wildlife include the establishment of protected areas (like national parks and wildlife reserves), laws against illegal hunting and trade, breeding programs, and public awareness campaigns</p>
<h4><b>Predators</h4></b>
<p>Predators are animals that hunt, kill, and eat other animals (called prey) for survival. They play a vital role in maintaining the health and balance of ecosystems by controlling the population of prey species and removing the weak or sick individuals.</p>

<h4><b>Types of Predators:</h4></b>
<p><b>1. Carnivores </b>– <mark>Eat only meat </mark>(e.g., lions, wolves, eagles).<br>


<b>2. Omnivores</b> – <mark>Eat both plants and animals but can act as predators</mark> (e.g., bears, humans).<br>


<b>3. Apex predators </b>– <mark>Top of the food chain with no natural predators </mark>(e.g., tigers, orcas, sharks).<br>


<b>4. Ambush predators </b>–<mark> Rely on stealth and surprise</mark> (e.g., crocodiles, leopards).<br>


<b>5. Pursuit predators </b>– <mark>Chase their prey </mark>(e.g., cheetahs, wild dogs).</p>
