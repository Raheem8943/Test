<br>
<p><b>The lion</b> is a large, powerful carnivore known for its golden fur and, in males, a distinctive mane. Native to the grasslands and savannas of Africa, lions are unique among big cats for their social structure, living in groups called prides. They are skilled hunters, often working together to catch prey such as zebras and antelope. As apex predators, lions play a key role in maintaining the balance of their ecosystem</p>


<h4><b><ins>Tiger</b></h4></ins>


<img src="https://images.unsplash.com/photo-1551972251-12070d63502a?fm=jpg&q=60&w=3000&ixlib=rb-4.1.0&ixid=M3wxMjA3fDB8MHxzZWFyY2h8Mnx8dGlnZXIlMjBoZWFkfGVufDB8fDB8fHww" height="300" width="400"><br>

<p><b>The tiger</b> is the largest species of big cat, known for its striking orange coat with black stripes. Native to parts of Asia, tigers are solitary and territorial animals that prefer dense forests and grasslands. They are powerful hunters, using stealth and strength to ambush prey like deer and wild boar. As apex predators, tigers are crucial for maintaining healthy ecosystems by controlling prey populations.</p>


<h4><b><ins>Jaguar</b></h4></ins>
<img src="https://cdn.pixabay.com/photo/2016/10/10/00/09/jaguar-1727406_1280.jpg" width="400" height="300">
<p><b>The jaguar</b> is a powerful big cat native to Central and South America, known for its muscular build and distinctive coat patterned with rosettes and spots. It is the largest cat in the Americas and has an incredibly strong bite, capable of crushing bones and even piercing turtle shells. Jaguars are solitary and often found near water, where they are skilled swimmers and hunters. They play an important role in maintaining the balance of tropical ecosystems by controlling populations of various prey species.</p>

<h4><b><ins>Cheetah</b></h4></ins>
<img src="https://encrypted-tbn0.gstatic.com/images?q=tbn:ANd9GcRfDM5G0-TO32s1u2u8ZjQnpk9DQQxf2hhrdA&s" width="400" height="300">

<p><b>The cheetah</b> is the fastest land animal, capable of reaching speeds up to 70 mph in short bursts. It has a slender body, long legs, and distinctive black tear marks on its face, which help reduce glare during hunting. Found mainly in the grasslands and savannas of Africa, cheetahs rely on speed and agility to catch prey like gazelles. Unlike other big cats, cheetahs are not strong fighters and often lose their kills to larger predators. Despite their speed, they are vulnerable and face threats from habitat loss and human-wildlife conflict.</p>

